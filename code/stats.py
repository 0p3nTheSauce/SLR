class HistoGram(Dict[Any, int]):
	"""Represents a histogram with value : count"""

	pass


def make_histogram(values: Iterable) -> HistoGram:
	"""Add up counts of unique values"""
	hist = HistoGram({})
	for val in values:
		if val in hist:
			hist[val] += 1
		else:
			hist[val] = 1
	return hist


class instance_stats(TypedDict):
	"""Represents statistics for instances in a list of InstanceDicts. 
	This can be stats per-class in a set, or, stats for the whole set.
	Keys
	---------
	- num_instances: int
	- length_distribution: HistoGram
	- signers_distribution: HistoGram
	- source_distribution: HistoGram
	- url_distribution: HistoGram
	- variation_distribution: HistoGram
	"""

	num_instances: int
	length_distribution: HistoGram
	signers_distribution: HistoGram
	source_distribution: HistoGram
	url_distribution: HistoGram
	variation_distribution: HistoGram



class set_stats(TypedDict):
	"""Represents statistics for a data subset (train/val/test)."""

	num_instances: int
	num_signers: int
	per_instance_stats: Dict[str, instance_stats]  # key is the class name


class split_stats(TypedDict):
	"""Represents statistics for a single dataset split (asl100, asl300, asl1000, asl2000)."""

	num_classes: int
	num_instances: int
	num_signers: int
	per_set_stats: Dict[AVAIL_SETS, set_stats]


# SET MANIPULATION


class preproc_class_dict(TypedDict):
	"""Represents a single gloss and its associated instances. Keeps the original
	unprocessed wlasl format, but with the preprocessed dict (added info)

	Keys:
	--------
	- gloss: str
	- instances: List[InstanceDict]
	"""

	gloss: str
	instances: List[InstanceDict]


def reverse_preproc_format(
	preproc_instances: List[InstanceDict],
) -> List[preproc_class_dict]:
	num_classes = len(set([inst["label_name"] for inst in preproc_instances]))
	lst_ppcd = [preproc_class_dict(gloss="empty", instances=[])] * num_classes
	for pp_inst in preproc_instances:
		label_num = pp_inst["label_num"]
		entry = lst_ppcd[label_num]
		if entry["gloss"] == "empty":
			lst_ppcd[label_num] = preproc_class_dict(
				gloss=pp_inst["label_name"], instances=[pp_inst]
			)
		else:
			lst_ppcd[label_num]["instances"].append(pp_inst)

	return lst_ppcd


def get_set(
	instances: List[instance_dict], set_name: AVAIL_SETS
) -> List[instance_dict]:
	"""Filters instances to only include those belonging to a specific set (train/val/test)."""
	filtered_instances = []
	for instance in instances:
		if instance["split"] == set_name:
			filtered_instances.append(instance)
	return filtered_instances


def seperate_by_set(
	glosses: List[wlasl_class_dict],
) -> Dict[AVAIL_SETS, List[wlasl_class_dict]]:
	"""Separates glosses by their set (train/val/test)."""
	set_names: List[AVAIL_SETS] = ["train", "val", "test"]
	sets: Dict[AVAIL_SETS, List[wlasl_class_dict]] = {name: [] for name in set_names}
	for gloss in glosses:
		instances = gloss["instances"]
		for set_name in sets.keys():
			filtered_instances = get_set(instances, set_name)

			sets[set_name].append(
				{"gloss": gloss["gloss"], "instances": filtered_instances}
			)
	return sets


def retrieve_split_data(
	split: AVAIL_SPLITS, labels_dir: Path, pattern: str
) -> Dict[str, Any]:

	split_dir = labels_dir / split
	objects = {}
	for file in split_dir.glob(pattern):
		with open(file, "r") as f:
			objects[file.name] = json.load(f)

	return objects


# STATISTICS CALCULATION


def collect_instance_stats(
	instances: Union[List[InstanceDict], List[instance_dict]],
) -> instance_stats:
	"""Collects statistics for a single class based on its instances.

	Args:
			instances (List[instance_dict]): A list of instances of the same class

	Returns:
			instance_stats: Statistics for the specific class, collected from all its instances
	"""
	num_instances = 0
	histograms = {
		"length": HistoGram({}),
		"signer_id": HistoGram({}),
		"source": HistoGram({}),
		"url": HistoGram({}),
		"variation_id": HistoGram({}),
	}

	for instance in instances:
		num_instances += 1
		instance_with_length = {
			**instance | {"length": instance["frame_end"] - instance["frame_start"]}
		}  # add length key
		for key, hist in histograms.items():  # do for each type of histogram
			value = instance_with_length[key]
			if value in hist:
				hist[value] += 1  # update bin count
			else:
				hist[value] = 1  # create entry if it doesnt exist

	return instance_stats(
		num_instances=num_instances,
		length_distribution=histograms["length"],
		signers_distribution=histograms["signer_id"],
		source_distribution=histograms["source"],
		url_distribution=histograms["url"],
		variation_distribution=histograms["variation_id"],
	)


def get_per_instance_stats(glosses: List[wlasl_class_dict]) -> Dict[str, instance_stats]:
	"""Collects statistics for all classes in a list of wlasl_class_dicts (recommend seperating into test/val/train first)"""
	per_instance_stats = {}
	for gloss in glosses:
		class_name = gloss["gloss"]
		instances = gloss["instances"]
		per_instance_stats[class_name] = collect_instance_stats(instances)
	return per_instance_stats


